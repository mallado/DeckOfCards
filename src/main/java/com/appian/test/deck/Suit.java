/**
 * Represents the four suits of a deck of poker-style playing cards: hearts,
 * spades, clubs, diamonds.
 * 
 * @author fmallado
 * @since 1.0.0.0
 */
public enum Suit {

	/** The spades. */
	SPADES,
	/** The hearts. */
	HEARTS,
	/** The diamonds. */
	DIAMONDS,
	/** The clubs. */
	CLUBS
}
